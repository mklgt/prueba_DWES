      /*
       * función que calcula el área de un cuadrado
       */
      function calcularAreaCuadrado($lado)
      {
          $area = $lado * $lado;
          return $area;
      }
      /*
       * función que calcula la media
       */
      function calcularMedia3($num1, $num2, $num3)
      {
          return ($num1 + $num2 + $num3) / 3;
      }
